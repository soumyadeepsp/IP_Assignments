			h = float(input())
			print ("The CSA of the hollow cylinder is:", "{:.2f}".format(2*math.pi*h*(r1+r2)))
			print ("The TSA of the hollow cylinder is:", "{:.2f}".format(2*math.pi*h*(r1+r2)+2*math.pi*(r1*r1-r2*r2)))
			print ("The volume of the hollow cylinder is:", "{:.2f}".format(math.pi*h*(r1*r1-r2*r2)))
