			print ("You chose a sphere")
			print ("Enter the radius of the sphere")
			r = float(input())
			print ("The CSA of the sphere is:", "{:.2f}".format(4*math.pi*r*r))
			print ("The TSA of the sphere is:", "{:.2f}".format(4*math.pi*r*r))
			print ("The volume of the sphere is:", "{:.2f}".format((4/3)*math.pi*r*r*r))
