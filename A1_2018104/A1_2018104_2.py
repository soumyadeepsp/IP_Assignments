			print ("You chose a cuboid")
			print ("Enter the length of the cuboid")
			l = float(input())
			print ("Enter the breadth of the cuboid")
			b = float(input())
			print ("Enter the height of the cuboid")
			h = float(input())
			print ("The CSA of the cuboid is:", "{:.2f}".format(2*(l+b)*h))
			print ("The TSA of the cuboid is:", "{:.2f}".format(2*(l*b+b*h+l*h)))
			print ("The volume of the cuboid is:", "{:.2f}".format(l*b*h))
